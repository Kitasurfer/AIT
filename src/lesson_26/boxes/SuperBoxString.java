// Комментарий с информацией о группе, авторе и дате создания класса. Это полезно для ведения документации

public class SuperBoxString {
    // Объявление публичного класса `SuperBoxString`, который работает с типом данных `String`

    private String value;
    // Приватное поле `value` типа `String`, которое хранит строковое значение. Приватный доступ ограничивает прямое изменение этого поля извне

    public SuperBoxString(String value) {
        // Конструктор класса, принимающий строковое значение и инициализирующий поле `value`
        this.value = value;
        // Используется `this.value`, чтобы различать поле класса и параметр конструктора
    }

    public String getValue() {
        // Метод-геттер для получения значения поля `value`. Возвращает текущее строковое значение
        return value;
    }

    public void setValue(String value) {
        // Метод-сеттер для изменения значения поля `value`. Позволяет обновлять строковое значение извне
        this.value = value;
    }

    @Override
    public String toString() {
        // Переопределение метода `toString()` для возврата строкового представления объекта
        return "SuperBoxString{" +
                "value='" + value + '\'' +
                '}';
        // Строковое представление включает имя класса и текущее значение поля `value`, что полезно для вывода и отладки
    }
}
